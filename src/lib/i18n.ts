const interpolate = (template: string, params?: Record<string, string | number>) => {
  if (!params) return template;
  return Object.entries(params).reduce(
    (acc, [key, value]) => acc.replaceAll(`{${key}}`, String(value)),
    template
  );
};

export const getLocale = (language: Language) => (language === "it" ? "it-CH" : "de-CH");

export function useI18n() {
  const language = useAppStore((state) => state.language);
  const setLanguage = useAppStore((state) => state.setLanguage);

  const t = useMemo(() => {
    return (key: string, params?: Record<string, string | number>) => {
      const template = messages[language]?.[key] ?? messages[fallbackLanguage]?.[key] ?? key;
      return interpolate(template, params);
    };
  }, [language]);

  const locale = useMemo(() => getLocale(language), [language]);

  return {
    language,
    setLanguage,
    locale,
    monthNamesShort: monthNamesShort[language],
    monthNamesLong: monthNamesLong[language],
    t,
  };
}
